/*
Отговорни сте за концерт на Веселин Маринов. На входа получавате две цели числа r и c, представляващи редовете и
колоните на седалките на публиката. След което получавате матрицата с r и c размери.
Всяка стойност в матрицата представлява желанието на човека, седящ на съответното място,
да гледа концерта на Веско.
По време на своето изпълнение Веско дава всичко от себе си, включително своята пот.
До получаване на -1. От конзола а трябва да се четат по три числа. Първите две представляват индексите
на редовете и колоните. А третото - количеството пот, което Веско изхърля на съответните индекси.
Когато количесто пот падне на дадено място, неговата стойност се намаля с количеството пот.
Седалките в залата (матрицата) НЕ могат да бъдат отрицателни числа. Т.е., ако числото на дадена
позиция стане по-малко от 0, то то трябва да бъде занулено.
Накарая отпечатайте матрицата.
*/
#include <iostream>
#include <vector>
using namespace std;

int main() {
    int r, c;
    cout << "Въведете брой редове и колони: ";
    cin >> r >> c;

    vector<vector<int>> matrix(r, vector<int>(c));


    cout << "Въведете матрицата (" << r << " x " << c << "):\n";
    for (int i = 0; i < r; ++i)
        for (int j = 0; j < c; ++j)
            cin >> matrix[i][j];

    cout << "Въвеждайте пръски пот (ред колона стойност), въведете -1 за край:\n";

    while (true) {
        int row, col, sweat;
        cin >> row;
        if (row == -1) break;
        cin >> col >> sweat;


        if (row >= 0 && row < r && col >= 0 && col < c) {
            matrix[row][col] -= sweat;
            if (matrix[row][col] < 0)
                matrix[row][col] = 0;
        } else {
            cout << "Невалиден индекс: (" << row << ", " << col << ")\n";
        }
    }


    cout << "\nФинална матрица:\n";
    for (int i = 0; i < r; ++i) {
        for (int j = 0; j < c; ++j)
            cout << matrix[i][j] << " ";
        cout << endl;
    }

    return 0;
}
